    private void setquestion() {
        qinit();
        question_count++;

        if (modeflag == 3) {
            modetag = (int) (Math.random() * 3);
        } else {
            modetag = modeflag;
        }

        WordingIO trueword = new WordingIO(vocabulary);

        if (modetag == 0) {

            MCmode(trueword);
        }

        if (modetag == 1) {
            BFmode(trueword);
        }

        if (modetag == 2) {
            TLmode(trueword);
        }

        if (time_limit != 0) {
            timingtoanswer(time_limit, jLabel2, answer_temp, jLabel5, jLabel6, jButton1);
        }
    }

    public void timingtoanswer(int time, JLabel jtfout, String answer, JLabel Jlbout, JLabel Jlbm, JButton jbt) {

        int delay = 1000; //milliseconds  

        ActionListener taskPerformer_count = new ActionListener1(time, jtfout);
        tm_count = new Timer(delay, taskPerformer_count);

        ActionListener taskPerformer_answer = new ActionListener2(answer, Jlbout, Jlbm, jbt);
        tm_answer = new Timer(time * delay + 1000, taskPerformer_answer);

        if (time_limit != 0) {
            tm_count.start();
            tm_answer.start();
        }

    }

