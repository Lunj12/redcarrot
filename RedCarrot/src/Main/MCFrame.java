    private void setquestion() {
        qinit();
        WordingIO trueword = new WordingIO();
        true_index = (int) Math.random() * option_number;
        DefaultListModel listModel1 = new DefaultListModel();

        // ä¸­â€”> en
        if (CE_flag == 0) {
            jLabel3.setText(trueword.getword_cn());
            answer_temp = trueword.getword_en();

            for (int i = 0; i < option_number; i++) {
                if (i == true_index) {
                    listModel1.addElement(trueword.getword_en());
                } else {
                    int tf_flag = 0;
                    while (tf_flag == 0) {
                        WordingIO falseword = new WordingIO();
                        if (trueword.word_index != falseword.word_index) {
                            tf_flag = 1;
                            listModel1.addElement(falseword.getword_en());
                        }
                    }

                }
            }

        }

        // enâ€”> ä¸­
        if (CE_flag == 1) {
            jLabel3.setText(trueword.getword_en());
            answer_temp = trueword.getword_cn();

            for (int i = 0; i < option_number; i++) {
                if (i == true_index) {
                    listModel1.addElement(trueword.getword_cn());
                } else {
                    int tf_flag = 0;
                    while (tf_flag == 0) {
                        WordingIO falseword = new WordingIO();
                        if (trueword.word_index != falseword.word_index) {
                            tf_flag = 1;
                            listModel1.addElement(falseword.getword_cn());
                        }
                    }

                }
            }

        }

        jList2.setModel(listModel1);
        timingtoanswer(time_limit, jLabel2, answer_temp, jLabel5, jLabel6, jButton1);

    }

    public void timingtoanswer(int time, JLabel jtfout, String answer, JLabel Jlbout, JLabel Jlbm, JButton jbt) {

        int delay = 1000; //milliseconds  

        ActionListener taskPerformer_count = new ActionListener1(time, jtfout);
        tm_count = new Timer(delay, taskPerformer_count);
        tm_count.start();

        ActionListener taskPerformer_answer = new ActionListener2(answer, Jlbout, Jlbm, jbt);
        tm_answer = new Timer(time * delay + 1000, taskPerformer_answer);
        tm_answer.start();

        System.out.println("done?");

    }

